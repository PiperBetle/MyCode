constexpr int N=5e2+1;
int n,m,e,dc,ans,chs[N],vis[N];
bsi g[N];
bool dfs(int u){
	for(int v:g[u])if(vis[v]!=dc){
		vis[v]=dc;
		if(!chs[v]||dfs(chs[v]))return chs[v]=u,true;
	}
	return false;
}
